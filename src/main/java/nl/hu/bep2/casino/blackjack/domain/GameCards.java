package nl.hu.bep2.casino.blackjack.domain;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;


public class GameCards {

	private int numberOfDecks;
	private ArrayList<Card> gameCards = null;
	
	public GameCards(int numberOfDecks) {
		this.numberOfDecks = numberOfDecks;
	}
	// ?????????????????????????????????????????????????????????????????????????????
	// ???????  HIER WIL IK EEN NAMED CONSTRUCTIOR MAKEN MAAR DAT LUKTE NU NOG NOET
	// ?????????????????????????????????????????????????????????????????????????????
	public void initiateDeck(int numberOfDecks) {
		
		this.numberOfDecks = numberOfDecks;
		int loop =numberOfDecks;
		Kleur kleuren[] = Kleur.values();
		Waarde waarden[] = Waarde.values();
		ArrayList<Card> cards = new ArrayList<>();
		
		while (loop > 0) {
			
			for(Kleur kleur: kleuren) {
				for( Waarde waarde: waarden) {
					Card card = new Card(kleur, waarde);
					cards.add(card);
				}
			}
			loop--;   // loop met één verlagen als er één compleet deck is toegevoegd
		}
		Collections.shuffle(cards);
		this.gameCards = cards;
		
	}
	// ?????????????????????????????????????????????????????????????????????????????
	// HOE KAN IK EEN LIST IN POSTMAN TONEN?  MOET HET WEL MET ARRAY LIST?
	//????????????????????????????????????????????????????????????????????????????
	public ArrayList<Card> getGameCards(){
		return this.gameCards;
	}
	
	
	public int getNumberOfDecks() {
		return this.numberOfDecks;
	}

	// ?????????????????????????????????????????????????????????????????????????????
	//WAAAROM KAN DIT IN POSTMAN NIET GETOOND WORDEN?
	// ?????????????????????????????????????????????????????????????????????????????
	public Card getFirstCard() {
		//return this.gameCards.get(1);
		Card card = new Card(Kleur.klaver,Waarde.aas);
		return card;
		
	}

	
}
